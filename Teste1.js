// Raiz
function raizQuadrada (x){
  return Math.sqrt(x);
}
console.log(raizQuadrada(2));

const raiz2 = (parametro) => Math.sqrt(parametro);
console.log(raiz2(2));

// Elevação
function Elevar2 (y,z){
  return Math.pow(y,z);
}

console.log(Elevar2(2,2));

const Elevar = (y,z) => Math.pow( y, z);
console.log(Elevar(2,2));

